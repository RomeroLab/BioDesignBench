# Analysis 2: Amino acid composition comparison
# ---------------------------------------------------------------------------
def plot_aa_composition(
    all_data: dict[str, dict[str, list[str]]],
    out_path: Path,
) -> None:
    """Plot amino acid composition comparison: conditions vs natural average.

    Uses a grouped bar chart with the 20 standard AAs on the x-axis,
    one bar group per condition, plus a reference line for the natural
    protein average.

    Args:
        all_data: Nested dict from load_all_sequences().
        out_path: Output PNG path.
    """
    conditions = list(all_data.keys())
    n_conditions = len(conditions)

    # Compute per-condition AA compositions
    compositions: dict[str, dict[str, float]] = {}
    for condition in conditions:
        all_seqs: list[str] = []
        for seqs in all_data[condition].values():
            all_seqs.extend(seqs)
        compositions[condition] = compute_aa_composition(all_seqs)

    fig, ax = plt.subplots(figsize=(14, 8))

    x = np.arange(len(STANDARD_AAS))
    total_width = 0.8
    bar_width = total_width / (n_conditions + 1)  # +1 for natural reference

    # Plot natural reference bars
    natural_vals = [NATURAL_AA_FREQ[aa] for aa in STANDARD_AAS]
    ax.bar(
        x - total_width / 2 + bar_width / 2,
        natural_vals,
        width=bar_width,
        color="#666666",
        alpha=0.6,
        label="Natural Average",
        edgecolor="white",
        linewidth=0.3,
    )

    # Plot condition bars
    for i, condition in enumerate(conditions):
        vals = [compositions[condition][aa] for aa in STANDARD_AAS]
        color = CONDITION_COLORS.get(condition, "#888888")
        hatch = "//" if "benchmark" in condition.lower() else None
        short_name = (
            condition
            .replace("Gemini 2.5 Pro", "Gemini")
            .replace("Sonnet 4.5", "Sonnet")
            .replace("DeepSeek V3", "DeepSeek")
        )
        ax.bar(
            x - total_width / 2 + (i + 1.5) * bar_width,
            vals,
            width=bar_width,
            color=color,
            alpha=0.7,
            label=short_name,
            edgecolor="white",
            linewidth=0.3,
            hatch=hatch,
        )

    ax.set_xticks(x)
    ax.set_xticklabels(STANDARD_AAS, fontsize=10, fontweight="bold")
    ax.set_xlabel("Amino Acid", fontsize=12, fontweight="bold")
    ax.set_ylabel("Frequency (%)", fontsize=12, fontweight="bold")
    ax.set_title(
        "Amino Acid Composition: Agent Designs vs Natural Proteins",
        fontsize=14,
        fontweight="bold",
    )
    ax.legend(
        loc="upper right",
        fontsize=7,
        frameon=True,
        framealpha=0.9,
        ncol=2,
    )
    ax.grid(axis="y", alpha=0.3)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    fig.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches="tight", facecolor="white")
    plt.close(fig)
    print(f"  Saved: {out_path}")


# ---------------------------------------------------------------------------
# Analysis 3 & 4: Summary statistics
# ---------------------------------------------------------------------------
def compute_summary(
    all_data: dict[str, dict[str, list[str]]],
) -> pd.DataFrame:
    """Compute per-condition summary statistics.

    Returns a DataFrame with columns:
        condition, n_tasks_with_fasta, total_sequences,
        mean_seq_length, std_seq_length, median_seq_length,
        mean_seq_count, n_nonstandard, n_empty, invalid_ratio,
        mean_aa_diversity
    """
    rows: list[dict] = []

    for condition in all_data:
        cond_data = all_data[condition]
        n_tasks = len(cond_data)

        # Collect all sequences for this condition
        all_seqs: list[str] = []
        seq_counts: list[int] = []
        diversities: list[int] = []

        for task_id, seqs in cond_data.items():
            all_seqs.extend(seqs)
            seq_counts.append(len(seqs))
            diversities.append(compute_aa_diversity(seqs))

        lengths = [len(s) for s in all_seqs]
        n_nonstandard, n_empty = count_invalid_sequences(all_seqs)

        total_seqs = len(all_seqs)
        invalid_ratio = (
            (n_nonstandard + n_empty) / total_seqs if total_seqs > 0 else 0.0
        )

        rows.append({
            "condition": condition,
            "n_tasks_with_fasta": n_tasks,
            "total_sequences": total_seqs,
            "mean_seq_length": np.mean(lengths) if lengths else 0.0,
            "std_seq_length": np.std(lengths) if lengths else 0.0,
            "median_seq_length": np.median(lengths) if lengths else 0.0,
            "mean_seq_count": np.mean(seq_counts) if seq_counts else 0.0,
            "n_nonstandard": n_nonstandard,
            "n_empty": n_empty,
            "invalid_ratio": invalid_ratio,
            "mean_aa_diversity": np.mean(diversities) if diversities else 0.0,
        })

    return pd.DataFrame(rows)


