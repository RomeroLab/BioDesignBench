def parse_fasta(path: Path) -> list[str]:
    """Parse a FASTA file and return a list of sequences.

    Handles multiline sequences, whitespace, and missing files gracefully.

    Args:
        path: Path to the FASTA file.

    Returns:
        List of amino acid sequences (uppercase, stripped). Returns empty
        list if the file does not exist or is empty.
    """
    if not path.exists():
        return []

    sequences: list[str] = []
    current: list[str] = []

    try:
        with open(path) as fh:
            for line in fh:
                line = line.strip()
                if not line:
                    continue
                if line.startswith(">"):
                    if current:
                        sequences.append("".join(current))
                    current = []
                else:
                    current.append(line)
            if current:
                sequences.append("".join(current))
    except (OSError, UnicodeDecodeError):
        return []

    return sequences


def compute_aa_composition(sequences: list[str]) -> dict[str, float]:
    """Compute amino acid frequency (percentage) across all sequences.

    Only counts the 20 standard amino acids. Non-standard characters
    are excluded from the total count but not from individual sequences.

    Args:
        sequences: List of amino acid sequences.

    Returns:
        Dictionary mapping each of the 20 standard AAs to its percentage
        frequency. Returns all zeros if no sequences or no valid residues.
    """
    counts: dict[str, int] = {aa: 0 for aa in STANDARD_AAS}

    total = 0
    for seq in sequences:
        for ch in seq.upper():
            if ch in counts:
                counts[ch] += 1
                total += 1

    if total == 0:
        return {aa: 0.0 for aa in STANDARD_AAS}

    return {aa: (counts[aa] / total) * 100.0 for aa in STANDARD_AAS}


def count_invalid_sequences(sequences: list[str]) -> tuple[int, int]:
    """Count sequences with non-standard amino acids and empty sequences.

    Args:
        sequences: List of amino acid sequences.

    Returns:
        Tuple of (n_nonstandard, n_empty) where n_nonstandard is the number
        of sequences containing at least one non-standard character and
        n_empty is the number of empty/whitespace-only sequences.
    """
    valid_set = set(STANDARD_AAS)
    n_nonstandard = 0
    n_empty = 0

    for seq in sequences:
        stripped = seq.strip()
        if not stripped:
            n_empty += 1
            continue
        if any(ch.upper() not in valid_set for ch in stripped):
            n_nonstandard += 1

    return n_nonstandard, n_empty


def compute_aa_diversity(sequences: list[str]) -> int:
    """Count the number of unique standard amino acids used across all sequences.

    Args:
        sequences: List of amino acid sequences.

    Returns:
        Number of unique standard AAs observed (0-20).
    """
    if not sequences:
        return 0

    valid_set = set(STANDARD_AAS)
    observed: set[str] = set()

    for seq in sequences:
        for ch in seq.upper():
            if ch in valid_set:
                observed.add(ch)

    return len(observed)


