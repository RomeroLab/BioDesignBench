# Main
# ---------------------------------------------------------------------------
def main() -> None:
    """Run all sequence analyses and save outputs."""
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    print("BDB-057: Protein Sequence Analysis")
    print("=" * 60)

    # Load all sequences
    print("\nLoading FASTA files from 9 conditions...")
    all_data = load_all_sequences()

    total_tasks = sum(len(v) for v in all_data.values())
    total_seqs = sum(len(s) for v in all_data.values() for s in v.values())
    print(f"  Loaded {total_seqs} sequences across {total_tasks} task-condition pairs")
    print()

    # Per-condition summary
    for condition, cond_data in all_data.items():
        n_tasks = len(cond_data)
        n_seqs = sum(len(s) for s in cond_data.values())
        print(f"  {condition:35s}  tasks={n_tasks:3d}  seqs={n_seqs:5d}")
    print()

    # --- Analysis 1: Sequence length distribution ---
    print("1. Sequence length distribution...")
    plot_seq_length_distribution(
        all_data, OUTPUT_DIR / "seq_length_distribution.png"
    )

    # --- Analysis 2: Amino acid composition ---
    print("2. Amino acid composition comparison...")
    plot_aa_composition(
        all_data, OUTPUT_DIR / "aa_composition_comparison.png"
    )

    # --- Analysis 3 & 4: Summary statistics ---
    print("3. Computing summary statistics...")
    summary = compute_summary(all_data)
    summary_path = OUTPUT_DIR / "sequence_analysis_summary.csv"
    summary.to_csv(summary_path, index=False, float_format="%.2f")
    print(f"  Saved: {summary_path}")
    print()

    # Print summary table
    print("=" * 60)
    print("SEQUENCE ANALYSIS SUMMARY")
    print("=" * 60)
    display_cols = [
        "condition", "n_tasks_with_fasta", "total_sequences",
        "mean_seq_length", "mean_seq_count", "invalid_ratio",
        "mean_aa_diversity",
    ]
    print(summary[display_cols].to_string(index=False, float_format="%.2f"))
    print()

    # Highlight key findings
    print("-" * 60)
    print("KEY FINDINGS:")
    print("-" * 60)

    # Which condition generates most sequences?
    if len(summary) > 0:
        max_seqs_row = summary.loc[summary["total_sequences"].idxmax()]
        min_seqs_row = summary.loc[summary["total_sequences"].idxmin()]
        print(
            f"  Most sequences:  {max_seqs_row['condition']} "
            f"({int(max_seqs_row['total_sequences'])} total, "
            f"{max_seqs_row['mean_seq_count']:.1f} per task)"
        )
        print(
            f"  Fewest sequences: {min_seqs_row['condition']} "
            f"({int(min_seqs_row['total_sequences'])} total, "
            f"{min_seqs_row['mean_seq_count']:.1f} per task)"
        )

        # Mean sequence length range
        print(
            f"  Sequence length range: "
            f"{summary['mean_seq_length'].min():.0f} - "
            f"{summary['mean_seq_length'].max():.0f} residues (mean)"
        )

        # Invalid sequences
        total_invalid = summary["n_nonstandard"].sum() + summary["n_empty"].sum()
        total_all = summary["total_sequences"].sum()
        if total_all > 0:
            print(
                f"  Invalid sequences: {int(total_invalid)} / {int(total_all)} "
                f"({100 * total_invalid / total_all:.1f}%)"
            )

        # AA diversity
        print(
            f"  AA diversity: {summary['mean_aa_diversity'].min():.1f} - "
            f"{summary['mean_aa_diversity'].max():.1f} unique AAs (mean per task)"
        )

    print()
    print(f"All outputs saved to {OUTPUT_DIR}/")


if __name__ == "__main__":
    main()
