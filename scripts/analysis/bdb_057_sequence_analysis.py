# Analysis 1: Sequence length distribution
# ---------------------------------------------------------------------------
def plot_seq_length_distribution(
    all_data: dict[str, dict[str, list[str]]],
    out_path: Path,
) -> None:
    """Plot histogram of sequence lengths per condition (faceted 3x3 grid).

    Args:
        all_data: Nested dict from load_all_sequences().
        out_path: Output PNG path.
    """
    conditions = list(all_data.keys())
    n_conditions = len(conditions)
    ncols = 3
    nrows = (n_conditions + ncols - 1) // ncols

    fig, axes = plt.subplots(nrows, ncols, figsize=(12, 8), squeeze=False)

    # Collect all lengths to determine shared bin range
    all_lengths: list[int] = []
    for cond_data in all_data.values():
        for seqs in cond_data.values():
            all_lengths.extend(len(s) for s in seqs)

    if not all_lengths:
        print("  No sequences found for length distribution plot.")
        plt.close(fig)
        return

    bin_min = 0
    bin_max = min(max(all_lengths) + 10, 1000)
    bins = np.linspace(bin_min, bin_max, 40)

    for idx, condition in enumerate(conditions):
        row, col = divmod(idx, ncols)
        ax = axes[row][col]

        cond_data = all_data[condition]
        lengths = []
        for seqs in cond_data.values():
            lengths.extend(len(s) for s in seqs)

        color = CONDITION_COLORS.get(condition, "#888888")
        if lengths:
            ax.hist(lengths, bins=bins, color=color, edgecolor="white",
                    alpha=0.8, linewidth=0.5)
            median_len = np.median(lengths)
            ax.axvline(median_len, color="black", linestyle="--",
                       linewidth=0.8, alpha=0.7)
            ax.text(
                0.95, 0.90,
                f"n={len(lengths)}\nmed={median_len:.0f}",
                transform=ax.transAxes,
                ha="right", va="top", fontsize=7,
                bbox=dict(boxstyle="round,pad=0.3", facecolor="white", alpha=0.8),
            )

        # Shorten condition name for title
        short_name = condition.replace("Gemini 2.5 Pro", "Gemini").replace("Sonnet 4.5", "Sonnet")
        ax.set_title(short_name, fontsize=9, fontweight="bold")
        ax.set_xlabel("Sequence Length", fontsize=7)
        ax.set_ylabel("Count", fontsize=7)
        ax.tick_params(labelsize=6)

    # Hide unused subplots
    for idx in range(n_conditions, nrows * ncols):
        row, col = divmod(idx, ncols)
        axes[row][col].set_visible(False)

    fig.suptitle("Designed Sequence Length Distributions", fontsize=13, fontweight="bold", y=1.01)
    fig.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches="tight", facecolor="white")
    plt.close(fig)
    print(f"  Saved: {out_path}")


