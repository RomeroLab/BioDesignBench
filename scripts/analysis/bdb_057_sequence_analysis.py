def load_all_sequences() -> dict[str, dict[str, list[str]]]:
    """Load all designed sequences from FASTA files for each condition and task.

    Returns:
        Nested dict: condition_name -> task_id -> list of sequences.
        Only includes tasks where the FASTA file exists and contains
        at least one sequence.
    """
    all_data: dict[str, dict[str, list[str]]] = {}

    for condition, info in CONDITION_MAP.items():
        agent_dir = info["path"]
        if not agent_dir.exists():
            print(f"  WARNING: {condition} dir not found: {agent_dir}")
            continue

        task_seqs: dict[str, list[str]] = {}
        for task_dir in sorted(agent_dir.iterdir()):
            if not task_dir.is_dir():
                continue
            fasta_path = task_dir / "output" / "designed_sequences.fasta"
            seqs = parse_fasta(fasta_path)
            if seqs:
                task_seqs[task_dir.name] = seqs

        all_data[condition] = task_seqs

    return all_data


